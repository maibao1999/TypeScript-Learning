//basic function 
function sum(a, b) {
    return a + b;
}
console.log('>>> Check sum ', sum(5, 10));
// arrow function 
var sum2 = function (a, b) {
    return a + b;
};
console.log('>>> Check sum2 ', sum2(5, 6));
////
var sum3 = function (x, y) {
    return x - y;
};
console.log('Check value', sum3(10, 5));
